package Chapter1_Breaking_the_Surface;

public class DooBee {
    public static void  main (String [] args) {
        int x = 1;
        while(x < 2) {
            System.out.println("Doo");
            System.out.println("Bee");
            x = x + 1;
        }
        if (x == 3) {
            System.out.print("Do");
        }
    }
}
